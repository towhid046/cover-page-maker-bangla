export const generateLogo = (zipCode) => {
  if (zipCode === 1000) {
    return `https://i.ibb.co/L1QbwQH/1000.png`;
  }

  if (zipCode === 9208) {
    return `https://i.ibb.co/Kxwb06Y/9208.png`;
  }

  if (zipCode === 7003) {
    return `https://i.ibb.co/SwKp1Lf/7003.png`;
  }

  if (zipCode === 8100) {
    return `https://i.ibb.co/tXCMM66/8100.png`;
  }

  if (zipCode === 6205) {
    return `https://i.ibb.co/94BmzfS/6205.png`;
  }

};
